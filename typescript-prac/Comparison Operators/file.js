var m = 5;
console.log(m >= 5 && m < 10); // && = and
console.log(m > 5 && m < 10);
console.log(m >= 5 || m < 10); // || = or
console.log(m > 5 || m < 10);
console.log(!(m < 10)); // the ! behing inverts the result
// like it is true that m is less than 10, but ! operator will invert the result and make it false
console.log(!(m > 10));
