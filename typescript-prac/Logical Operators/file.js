var n = 5;
console.log(n == 5); // Equals true, n is equal to 5
console.log(n === 5); // checks n's type that is a number and checks value that is 5, results true
console.log(n != 5); // is n not equal to 5
console.log(n > 8); // is n greater than 8
console.log(n < 8); // is n less than 8
console.log(n >= 8); // is n greater than or equal to 8
console.log(n <= 8); // is n less than or equal to 8, this is basically OR operator 
